package com.singlepoint.androidkotlin.architecure

/****
 *
 * MVC:
 *
 * Direct communication between View and Model.
 * Controller updates the Model based on user input.
 * MVP:
 *
 * View is passive and interacts with the Presenter.
 * Presenter handles all logic and updates both View and Model.
 * MVVM:
 *
 * ViewModel abstracts the business logic and updates the View through data binding.
 * View is passive and relies on ViewModel for data and logic.
 *
 *
 * */